# ITERATE THROUGH THE ITEM AND PRINT THE VALUES:
thistuple = ("apple", "banana", "cherry")
for x in thistuple:
    print(x)

# ALL ITEM USING A WHILE LOOP TO GO THROUGH ALL THE INDEX NUMBER
thistuple = ("apple", "banana", "cherry")
i = 0
while i < len(thistuple):
    print(thistuple[i])
    i = i + 1